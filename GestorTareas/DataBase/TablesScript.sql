--TABLA LINKEDTASK
CREATE TABLE LinkedTasks
(
    Id INT IDENTITY(1,1) PRIMARY KEY,
    LinkedTaskOrder INT NOT NULL,
    CONSTRAINT FK_LINKED_COMPOSITETASKS_ID
    FOREIGN KEY (Id) REFERENCES COMPOSITETASKS(Id)
);
GO

--TABLA RECURRING TASK
CREATE TABLE RecurringTasks
(
    Id INT IDENTITY(1,1) PRIMARY KEY,
    RecurrenceRule INT NOT NULL,
    RecurringTasksCount INT NOT NULL,
    CONSTRAINT FK_RECURRINGTASKS_TASKS_ID
FOREIGN KEY (Id) REFERENCES Tasks(Id)
);
GO


